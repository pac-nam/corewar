#include "libft.h"

void	ft_put_uhex(unsigned int nb)
{
	char	*str;

	str = "0123456789abcdef";
	if (nb >= 16)
		ft_put_uhex(nb / 16);
	ft_putchar(str[nb % 16]);
}
