#include "libft.h"

char		*ft_strcend(char *s, char c)
{
	int		i;

	if (s)
	{
		i = ft_strlen(s) - 1;
		while (s[i] && s[i] != c)
			i--;
		return (ft_strdup(s + i + 1));
	}
	return (NULL);
}
