#include "libft.h"

int			ft_strwth(char *s, char *with)
{
	int		i;

	i = 0;
	if (s)
	{
		while (s[i])
		{
			if (ft_strnchar(s[i], with) == 0)
				return (0);
			i++;
		}
	}
	return (1);
}
