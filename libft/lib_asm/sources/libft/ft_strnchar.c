#include "libft.h"

int			ft_strnchar(char c, char *s)
{
	int		i;
	int		cpt;

	i = 0;
	cpt = 0;
	while (s[i])
	{
		if (s[i] == c)
			cpt++;
		i++;
	}
	return (cpt);
}
