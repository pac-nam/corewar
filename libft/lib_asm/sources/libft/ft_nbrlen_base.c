#include "libft.h"

int		ft_nbrlen_base(int n, int base)
{
	int		cpt;

	cpt = 0;
	if (n < 0)
		cpt = 1;
	while (n)
	{
		cpt++;
		n = n / base;
	}
	return (cpt);
}
